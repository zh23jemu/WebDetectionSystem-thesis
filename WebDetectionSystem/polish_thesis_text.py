def main() -> None:
    """执行论文正文段落润色并保存结果。"""
    document = Document(DOC_PATH)

    for index, new_text in REPLACEMENTS.items():
        if index >= len(document.paragraphs):
            raise IndexError(f"段落索引越界：{index}")
        document.paragraphs[index].text = new_text

    document.save(DOC_PATH)
    print(f"已完成正文润色，共更新 {len(REPLACEMENTS)} 个段落。")


if __name__ == "__main__":
    main()
