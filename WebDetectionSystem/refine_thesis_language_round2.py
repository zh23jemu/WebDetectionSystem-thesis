def set_para_text(paragraph, text):
    """替换段落内容，尽量保留原段落位置和基本版式。"""
    for run in paragraph.runs:
        run.text = ""
    if paragraph.runs:
        paragraph.runs[0].text = text
    else:
        paragraph.add_run(text)


def replace_by_contains(doc, mapping):
    """按段落中唯一特征短语做替换，避免整段匹配不稳定。"""
    replaced = 0
    for para in doc.paragraphs:
        text = para.text.strip()
        if not text:
            continue
        for needle, new_text in mapping.items():
            if needle in text:
                set_para_text(para, new_text)
                replaced += 1
                break
    return replaced


